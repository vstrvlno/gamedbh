@dp.message(Command("start"))
async def start_game(message: types.Message):
    """–ù–∞—á–∞–ª–æ –Ω–æ–≤–æ–π –∏–≥—Ä—ã"""
    user_id = message.from_user.id
    players[user_id] = {"current": 1, "status": "playing"}
    await send_story(message.chat.id, user_id)


async def send_story(chat_id: int, user_id: int):
    """–û—Ç–ø—Ä–∞–≤–∫–∞ —Ç–µ–∫—É—â–µ–π —Å—Ü–µ–Ω—ã –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é"""
    state = players[user_id]
    current = state["current"]

    # –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å–æ—Å—Ç–æ—è–Ω–∏—è
    if state["status"] != "playing":
        return

    # –ü—Ä–æ–∏–≥—Ä—ã—à
    if current == 0:
        await bot.send_message(chat_id, "–í—ã –ø—Ä–æ–∏–≥—Ä–∞–ª–∏. –ö–æ–Ω–µ—Ü –∏—Å—Ç–æ—Ä–∏–∏.")
        state["status"] = "lost"
        return

    # –ü–æ–±–µ–¥–∞
    if current == -1:
        await bot.send_message(chat_id, "–ü–æ–∑–¥—Ä–∞–≤–ª—è–µ–º! –ò—Å—Ç–æ—Ä–∏—è –∑–∞–≤–µ—Ä—à–µ–Ω–∞ —É—Å–ø–µ—à–Ω–æ!")
        state["status"] = "finished"
        return

    event = story.get(current)
    if not event:
        await bot.send_message(chat_id, "–ò—Å—Ç–æ—Ä–∏—è –∑–∞–∫–æ–Ω—á–∏–ª–∞—Å—å.")
        state["status"] = "finished"
        return

    text = event["text"]

    # –ö–ª–∞–≤–∏–∞—Ç—É—Ä–∞ —Å –≤–∞—Ä–∏–∞–Ω—Ç–∞–º–∏
    keyboard = InlineKeyboardMarkup(inline_keyboard=[])
    for key, choice in event.get("choices", {}).items():
        keyboard.inline_keyboard.append([
            InlineKeyboardButton(text=choice["text"], callback_data=key)
        ])

    await bot.send_message(chat_id, text, reply_markup=keyboard)


@dp.callback_query()
async def handle_choice(callback: types.CallbackQuery):
    """–û–±—Ä–∞–±–æ—Ç–∫–∞ –≤—ã–±–æ—Ä–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è"""
    user_id = callback.from_user.id

    if user_id not in players or players[user_id]["status"] != "playing":
        await callback.answer("–ò–≥—Ä–∞ –Ω–µ–∞–∫—Ç–∏–≤–Ω–∞. –ù–∞–±–µ—Ä–∏—Ç–µ /start")
        return

    state = players[user_id]
    current = state["current"]
    event = story.get(current)

    if not event:
        await callback.answer("–û—à–∏–±–∫–∞ —Å—Ü–µ–Ω–∞—Ä–∏—è.")
        return

    choice_key = callback.data
    if choice_key not in event["choices"]:
        await callback.answer("–ù–µ–≤–µ—Ä–Ω—ã–π –≤—ã–±–æ—Ä.")
        return

    next_step = event["choices"][choice_key]["next"]
    state["current"] = next_step

    await callback.answer()
    await send_story(callback.message.chat.id, user_id)

